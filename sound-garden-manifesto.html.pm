◊h1{Sound Garden Manifesto}

◊p{Reflections on guiding principles of ◊link["https://github.com/ul/sound-garden-0x2"]{Sound Garden}}

◊items{
  ◊item{Sound Garden never meant to be a product, a thing by itself. It's a by-product of the creative journey.}
  ◊item{Creativity blossoms without limit when put in consciously defined constraints. Self-posed restrictions are what helps to maintain focus and embody art artifacts.}
  ◊item{Sound Garden's leitmotif constraint is doing everything live and in real-time. If your sound graph can’t be computed sample by sample for each channel in real-time, then drop it and find a new one. This does not prevent performance optimizations of the platform, rather contrary it encourages them.}
  ◊item{Don't be afraid to be naive, you are exploring a mysterious landscape.}
  ◊item{True understanding comes as the ability to re-create subject from scratch. With own style.}
  ◊item{Style ◊em{is} constraints working for you.}
  ◊item{This document is just a reflection of Sound Garden spirit and practices, no way it's primary to them. The manifesto is living and is to be updated whenever it needs to be.}
}
